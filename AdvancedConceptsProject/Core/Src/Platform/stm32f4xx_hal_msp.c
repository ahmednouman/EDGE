{
  if(hadc->Instance==ADC1)
  {
    __HAL_RCC_ADC1_CLK_DISABLE();

    HAL_GPIO_DeInit(GPIOA, GPIO_PIN_0);

    HAL_NVIC_DisableIRQ(ADC_IRQn);
  }

}

